/**
 * Fetches POI data from RuneLite's worldmap plugin Java enums
 * and generates new-area-pois.js with all locations.
 *
 * Usage: node scripts/fetch-runelite-pois.js
 */

const https = require('https');
const fs = require('fs');

const BASE = 'https://raw.githubusercontent.com/runelite/runelite/master/runelite-client/src/main/java/net/runelite/client/plugins/worldmap/';

const FILES = {
  fishing: { file: 'FishingSpotLocation.java', icon: 'fishing_spot' },
  mining: { file: 'MiningSiteLocation.java', icon: 'mining_site' },
  farming: { file: 'FarmingPatchLocation.java', icon: 'farming_patch' },
  rareTree: { file: 'RareTreeLocation.java', icon: 'rare_trees' },
  hunter: { file: 'HunterAreaLocation.java', icon: 'hunter_training' },
  agility: { file: 'AgilityCourseLocation.java', icon: 'agility_short-cut' },
  altar: { file: 'RunecraftingAltarLocation.java', icon: 'altar' },
  transport: { file: 'TransportationPointLocation.java', icon: 'transportation' },
  mooring: { file: 'MooringLocation.java', icon: 'transportation' },
};

function fetch(url) {
  return new Promise((resolve, reject) => {
    https.get(url, res => {
      let data = '';
      res.on('data', chunk => data += chunk);
      res.on('end', () => resolve(data));
    }).on('error', reject);
  });
}

// Parse Java enum entries — extracts enum name + WorldPoint coordinates
function parseEnum(javaSource, icon) {
  const entries = [];

  // Split into enum entries (each starts with a NAME( pattern)
  // Join all lines first to handle multi-line entries
  const body = javaSource.replace(/\r/g, '');

  // Find enum body
  const enumStart = body.indexOf('{');
  const enumEnd = body.lastIndexOf(';');
  if (enumStart < 0 || enumEnd < 0) return entries;
  const enumBody = body.substring(enumStart + 1, enumEnd);

  // Split on commas that are followed by a new enum entry (UPPER_CASE name with parenthesis)
  // Each entry: NAME(...) or NAME\n(...)
  const entryRegex = /([A-Z][A-Z0-9_]+)\s*\(/g;
  let match;
  const entryStarts = [];
  while ((match = entryRegex.exec(enumBody)) !== null) {
    entryStarts.push({ name: match[1], pos: match.index });
  }

  for (let i = 0; i < entryStarts.length; i++) {
    const start = entryStarts[i].pos;
    const end = i + 1 < entryStarts.length ? entryStarts[i + 1].pos : enumBody.length;
    const chunk = enumBody.substring(start, end);
    const rawName = entryStarts[i].name;

    // Clean up the name: ALDARIN_NORTH -> Aldarin North
    const name = rawName.replace(/_/g, ' ').replace(/\d+$/, '').trim()
      .toLowerCase().replace(/\b\w/g, c => c.toUpperCase()).trim();

    // Extract all WorldPoints
    const wpMatches = [...chunk.matchAll(/new WorldPoint\((\d+),\s*(\d+),\s*(\d+)\)/g)];
    for (const m of wpMatches) {
      const x = parseInt(m[1]);
      const y = parseInt(m[2]);
      const plane = parseInt(m[3]);

      // Only surface (plane 0, y < 5000 to skip instances)
      if (plane !== 0 || y > 5000) continue;

      entries.push({ x, y, icon, name });
    }

    // Also check for quoted tooltip strings (used by TransportationPointLocation)
    const tooltipMatch = chunk.match(/"([^"]{3,})"/);
    if (tooltipMatch) {
      // Re-process with the tooltip as name
      const tooltipName = tooltipMatch[1];
      // Replace all entries from this chunk with the tooltip name
      for (let j = entries.length - wpMatches.length; j < entries.length; j++) {
        if (j >= 0) entries[j].name = tooltipName;
      }
    }
  }
  return entries;
}

async function main() {
  console.log('Fetching RuneLite worldmap data...');
  const allPois = [];

  for (const [key, config] of Object.entries(FILES)) {
    try {
      const url = BASE + config.file;
      console.log(`  Fetching ${config.file}...`);
      const src = await fetch(url);
      const pois = parseEnum(src, config.icon);
      console.log(`    Found ${pois.length} surface POIs`);
      allPois.push(...pois);
    } catch (e) {
      console.error(`  Failed to fetch ${config.file}: ${e.message}`);
    }
  }

  // Deduplicate by coordinate
  const seen = new Set();
  const unique = allPois.filter(p => {
    const key = `${p.x},${p.y},${p.icon}`;
    if (seen.has(key)) return false;
    seen.add(key);
    return true;
  });

  console.log(`\nTotal unique surface POIs: ${unique.length}`);

  // Generate JS
  const lines = unique.map(p =>
    `  { x: ${p.x}, y: ${p.y}, icon: "${p.icon}", name: "${p.name.replace(/"/g, '\\"')}" },`
  );

  const output = `/**
 * RuneRadar — Supplemental POI Data from RuneLite WorldMap Plugin
 *
 * Auto-generated by scripts/fetch-runelite-pois.js
 * Source: github.com/runelite/runelite worldmap plugin enums
 * Generated: ${new Date().toISOString().split('T')[0]}
 *
 * ${unique.length} POIs from: fishing, mining, farming, trees, hunter, agility, altars, transport
 */

const NEW_AREA_POIS = [
${lines.join('\n')}
];
`;

  fs.writeFileSync('webapp/new-area-pois.js', output);
  console.log(`\nWrote ${unique.length} POIs to webapp/new-area-pois.js`);
}

main().catch(console.error);
